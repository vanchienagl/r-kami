(function($) {
   $.fn.execAuth = function(options) {
        var options = jQuery.extend( {
            /* MD5：有効無効 */
            md5:true,
            /* パスワード */
            password: 'c948fdc2e00e27efa5c7563d1fe2ed6e',//s166
            /* 右クリック防止：有効無効 */
            contextmenu_block:true,
            /* クッキーに利用するキー */
            cookie_key:'simple_cookie_auth',
            /* エラーメッセージ出力場所のID */
            error_msg_id:'error',
            /* エラーメッセージ */
            error_msg_text:'パスワードが間違っています',
            /* ログイン後遷移先URL */
            action_url: './index.php',
            /* リダイレクト先URL */
            redirect_url: './login.php'
        }, options);

        // ページ全体の表示を行う（JavaScriptが有効でないとbodyを表示しない）
        $("body").show();

        // 右クリック禁止が有効の場合
        if(options.contextmenu_block)
        {
            // 右クリック禁止
            $(this).bind("contextmenu",function(e){
              return false;
            });
        }

        // パスワード入力用テキストボックス取得
        var password_text = $("#password").attr("id");

        // ログインボタン取得
        var login_btn = $("#loginbtn").attr("id");

        // ログインページに必要なUIが揃っている場合
        if ((password_text != undefined) && (login_btn != undefined))
        {
            // ログインボタン押下時
            $("#loginbtn").click(function(){
                // クッキーを初期化
                $.cookie(options.cookie_key, "");

                // 画面から入力されたパスワード保存用変数
                var text_password = '';

                // MD5暗号化が有効な場合
                if (options.md5)
                {
                     // 画面から入力されたパスワードを取得してMD5で暗号化
                    text_password = $.md5($("#password").val());
                } else {
                     // 画面から入力されたパスワードを取得
                     text_password = $("#password").val();
                }

                // パスワードを比較
                if (text_password == options.password)
                {
                    // クッキーに保存
                    $.cookie(options.cookie_key, $("#password").val());

                    // 遷移先を設定
                    $("form").attr("action", options.action_url);

                    // 画面遷移
                    $("form").submit();
                } else {
                    // エラーメッセージを表示
                    $("#" + options.error_msg_id).text(options.error_msg_text);
                }

            });

            // キーを押した時
            $("input").keypress(function(e) {
                if (!e) var e = window.event;
                // Enterキーでのsubmitを抑止
                if(e.keyCode == 13)
                {
                    return false;
                }

            });

            // ログインページであると見なして処理を中断する
            return;
        }

        // 取得パスワード保存用変数
        var cookie_password = '';

        // MD5暗号化が有効な場合
        if (options.md5)
        {
            // クッキーからパスワードを取得し、md5で暗号化
            cookie_password = $.md5($.cookie(options.cookie_key));
        } else {
            // クッキーからパスワードを取得
            cookie_password = $.cookie(options.cookie_key);
        }

        // クッキーから取得したパスワードが認証できない場合
        if (cookie_password != options.password)
        {
            // リダイレクトを行う
            location.href = options.redirect_url;
        }
   }
})(jQuery);


